    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:                                          
        String namaHewan = nama_hewan.getText();
        String namaAdopter = nama_adopter.getText();
        String jenis = jenis_jenis.getText();
        if (umur_umur.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Umur tidak boleh kosong!");
            return;
    }
        int umur = Integer.parseInt(umur_umur.getText());
        String gender = gender_gender.getSelectedItem().toString();

    try {
        Connection conn = config.Koneksi.getKoneksi();
        String sql = "INSERT INTO adopsi_hewan (nama_hewan, nama_adopter, jenis, umur, gender) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, namaHewan);
        ps.setString(2, namaAdopter);
        ps.setString(3, jenis);
        ps.setInt(4, umur);
        ps.setString(5, gender);
        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
        loadData(); 
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    
    }                                      


    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        int baris = rekap_data.getSelectedRow();

if (baris == -1) {
    JOptionPane.showMessageDialog(
        this,
        "Pilih data yang mau diupdate dulu!"
    );
    return;
}

DefaultTableModel model =
        (DefaultTableModel) rekap_data.getModel();

int id = Integer.parseInt(
        model.getValueAt(baris, 0).toString()
);

String namaHewan   = nama_hewan.getText();
String namaAdopter = nama_adopter.getText();
String jenis       = jenis_jenis.getText();

int umur = Integer.parseInt(
        umur_umur.getText()
);

String gender = gender_gender
        .getSelectedItem()
        .toString();

try {
    Connection conn = config.Koneksi.getKoneksi();

    String sql =
        "UPDATE adopsi_hewan SET " +
        "nama_hewan=?, " +
        "nama_adopter=?, " +
        "jenis=?, " +
        "umur=?, " +
        "gender=? " +
        "WHERE id_hewan=?";

    PreparedStatement ps =
            conn.prepareStatement(sql);

    ps.setString(1, namaHewan);
    ps.setString(2, namaAdopter);
    ps.setString(3, jenis);
    ps.setInt(4, umur);
    ps.setString(5, gender);
    ps.setInt(6, id);

    ps.executeUpdate();

    JOptionPane.showMessageDialog(
        this,
        "Data berhasil diupdate!"
    );

    loadData();
    conn.close();

} catch (Exception e) {

    JOptionPane.showMessageDialog(
        this,
        "Error: " + e.getMessage()
    );
}
    }                                      

